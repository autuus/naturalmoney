<p><b>Projektista</b></p>
<p>
Tämän projektin tarkoituksena on tarjota kansalle vaihtoehtoinen hallitus, koska nykyinen järjestelmä näyttää lahoamisen merkkejä. Rahajärjestelmän tarkoitus on palauttaa rahan valta yksityisltä pankeilta suomen kansalle. Tässä ei ole tarkoitus vastustaa vanhaa, vaan luoda uutta tilalle, ja antaa jokaiselle kansalaiselle mahdollisuus valita, että kumpaa käyttää ja kuinka paljon.
<br><br>
Rahajärjestelmä toimii seuraavilla säännöillä:<br>
1. Koron pyytäminen lainasta on ehdottomasti kiellettyä, tällä tavoin saamme rahan luomisen tyhjästä kuriin.<br>
2. Verotus hoidetaan siirtämällä joka kuukausi kaikesta rahasta 4% valtion kassaan, tällä tavoin saamme rahan virtaamaan paremmin, eikä rahapatoutumia synny niin helposti.<br>
3. Pankeille ei ole tässä järjestelmässä niinkään tarvetta. Lainaus bisnes saa velottaa ainoastaan lainatun rahan omistajalta välityspalkkiota, koska lainaajalta palkkion pyytäminen olisi tietynlainen korko.<br>
4. Yhteenlaskettu rahamäärä ja kaikki valtion rahaliikenne on täysin julkista.<br>
5. Uutta rahaa ei paineta sen jälkeen kun tarpeeksi rahaa on liikenteessä joten tämä "piilotettu vero" eli inflaatio pidetään kurissa.<br>
<br><br>
Käteinen raha toimii niin, että kirjoitat halutun summan että paljonko haluat nostaa, sen jälkeen ohjelma tekee sinulle setelin, jossa on 12 numeronen koodi. Tämän koodin voi sinä tai joku muu syöttää omaan pankkitiliinsä, ja tallettaa rahat, joten rahan väärentäminen on mahdotonta.
<br><br>
Kansalaisuus palkka olisi ihanteellinen, mutta sitä ei ole vielä mietitty loppuun että miten se toimisi.
<br><br>
<b>Eduskunta</b>
<br><br>
Jokainen joka käyttää euroja, maksaa veroa Suomen nykyiselle hallitukselle, mutta jokainen joka käyttää tätä kansanvaltaista rahajärjestelmää maksaa veroja uudelle vaihtoehtoiselle hallitukselle, jossa jokaisella ihmisellä on äänioikeus. Hallitukseen valitaan sellaisia ihmisiä, jotka ovat orientoituneita enemmän muiden ihmisten auttamiseen itsekeskeisyyden siaan. Ja olisi kiva jos viisaat ihmiset saisivat päättää yhteiskunnan asioista.
